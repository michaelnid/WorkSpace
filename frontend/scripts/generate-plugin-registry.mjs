import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const scriptFile = fileURLToPath(import.meta.url);
const rootDir = path.resolve(path.dirname(scriptFile), '../..');
const pluginsDir = path.join(rootDir, 'plugins');
const targetFile = path.join(rootDir, 'frontend', 'src', 'pluginRegistry.ts');

function toPosix(filePath) {
    return filePath.split(path.sep).join('/');
}

function toSafeVarName(value) {
    return value.replace(/[^a-zA-Z0-9_]/g, '_');
}

async function readPluginEntries() {
    let dirEntries = [];
    try {
        dirEntries = await fs.readdir(pluginsDir, { withFileTypes: true });
    } catch {
        return [];
    }

    const discovered = [];

    for (const entry of dirEntries) {
        if (!entry.isDirectory() || entry.name.startsWith('_')) {
            continue;
        }

        const pluginDir = path.join(pluginsDir, entry.name);
        const manifestPath = path.join(pluginDir, 'plugin.json');

        try {
            const manifest = JSON.parse(await fs.readFile(manifestPath, 'utf8'));
            if (!manifest.id || !manifest.frontend_entry) {
                continue;
            }

            const frontendEntryPath = path.join(pluginDir, manifest.frontend_entry);
            await fs.access(frontendEntryPath);

            discovered.push({
                id: String(manifest.id),
                name: String(manifest.name || manifest.id),
                importPath: toPosix(path.relative(path.join(rootDir, 'frontend', 'src'), frontendEntryPath)).replace(/\.(tsx?|jsx?)$/, ''),
            });
        } catch {
            // Ungueltiges Plugin oder fehlender Frontend-Entry.
        }
    }

    discovered.sort((a, b) => a.id.localeCompare(b.id));
    return discovered;
}

function buildRegistryFile(plugins) {
    const imports = plugins.map((plugin, index) => {
        const moduleVar = `plugin_${toSafeVarName(plugin.id)}_${index}`;
        plugin.moduleVar = moduleVar;
        const importPath = plugin.importPath.startsWith('.') ? plugin.importPath : `./${plugin.importPath}`;
        return `import * as ${moduleVar} from '${importPath}';`;
    });

    const registryEntries = plugins.map((plugin) => `    {
        id: '${plugin.id}',
        name: '${plugin.name.replace(/'/g, "\\'")}',
        routes: (Array.isArray((${plugin.moduleVar} as any).routes) ? (${plugin.moduleVar} as any).routes : []) as PluginRoute[],
        navItems: (Array.isArray((${plugin.moduleVar} as any).navItems) ? (${plugin.moduleVar} as any).navItems : []) as PluginNavItem[],
        dashboardTiles: (Array.isArray((${plugin.moduleVar} as any).dashboardTiles) ? (${plugin.moduleVar} as any).dashboardTiles : []) as PluginDashboardTile[],
        extensionTiles: (Array.isArray((${plugin.moduleVar} as any).extensionTiles) ? (${plugin.moduleVar} as any).extensionTiles : []) as PluginExtensionTile[],
        settingsPanel: ((${plugin.moduleVar} as any).settingsPanel && typeof (${plugin.moduleVar} as any).settingsPanel === 'object' ? (${plugin.moduleVar} as any).settingsPanel : undefined) as PluginSettingsPanel | undefined,
        searchProvider: ((${plugin.moduleVar} as any).searchProvider && typeof (${plugin.moduleVar} as any).searchProvider === 'object' ? (${plugin.moduleVar} as any).searchProvider : undefined) as PluginSearchProvider | undefined,
        quickActions: (Array.isArray((${plugin.moduleVar} as any).quickActions) ? (${plugin.moduleVar} as any).quickActions : []) as PluginQuickAction[],
    }`);

    return `// Auto-generated by scripts/generate-plugin-registry.mjs
// Nicht manuell bearbeiten.

import type { ComponentType, LazyExoticComponent } from 'react';
${imports.join('\n')}

export interface PluginRoute {
    path: string;
    component: LazyExoticComponent<ComponentType<any>>;
    permission?: string;
}

export interface PluginNavItem {
    label: string;
    icon: string;
    path: string;
    permission?: string;
    order: number;
    group?: string;
    groupIcon?: string;
    groupOrder?: number;
}

export interface PluginDashboardTile {
    id: string;
    title: string;
    description?: string;
    component: LazyExoticComponent<ComponentType<any>>;
    permission?: string;
    order?: number;
    /** @deprecated Verwende defaultWidth/defaultHeight. Wird automatisch migriert. */
    defaultSize?: 'small' | 'medium' | 'large';
    /** Breite in Grid-Units (1 Unit = 20px). Default: 12 */
    defaultWidth?: number;
    /** Höhe in Grid-Units (1 Unit = 20px). Default: 8 */
    defaultHeight?: number;
    defaultVisible?: boolean;
}

export interface PluginExtensionTile {
    id: string;
    targetSlot: string;
    title: string;
    description?: string;
    component: LazyExoticComponent<ComponentType<any>>;
    permission?: string;
    order?: number;
    defaultSize?: 'small' | 'medium' | 'large';
}

export interface PluginSettingsPanel {
    component: LazyExoticComponent<ComponentType<any>>;
    permission?: string;
}

export interface PluginSearchResult {
    title: string;
    description?: string;
    path: string;
    icon?: string;
}

export interface PluginSearchProvider {
    label: string;
    search: (query: string) => Promise<PluginSearchResult[]>;
    permission?: string;
}

export interface PluginQuickAction {
    id: string;
    label: string;
    icon?: string;
    keywords?: string[];
    permission?: string;
    execute: () => void | Promise<void>;
}

export interface PluginRegistryEntry {
    id: string;
    name: string;
    routes: PluginRoute[];
    navItems: PluginNavItem[];
    dashboardTiles: PluginDashboardTile[];
    extensionTiles: PluginExtensionTile[];
    settingsPanel?: PluginSettingsPanel;
    searchProvider?: PluginSearchProvider;
    quickActions?: PluginQuickAction[];
}

export const pluginRegistry: PluginRegistryEntry[] = [
${registryEntries.join(',\n')}
];
`;
}

async function main() {
    const plugins = await readPluginEntries();
    const content = buildRegistryFile(plugins);
    await fs.writeFile(targetFile, content, 'utf8');
    console.log(`[plugin-registry] ${plugins.length} Plugin(s) geschrieben: ${targetFile}`);
}

main().catch((error) => {
    console.error('[plugin-registry] Fehler:', error);
    process.exit(1);
});
